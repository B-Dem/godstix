    function init() {
        var saved = localStorage.getItem('nova_language');
        if (saved && translations[saved]) {
            currentLang = saved;
        }
    }

    function t(key, params) {
        var str = (translations[currentLang] && translations[currentLang][key]) || (translations.pt && translations.pt[key]) || key;
        if (params) {
            Object.keys(params).forEach(function(k) {
                str = str.replace('{' + k + '}', params[k]);
            });
        }
        return str;
    }

    function setLanguage(lang) {
        if (translations[lang]) {
            currentLang = lang;
            localStorage.setItem('nova_language', lang);
        }
    }

